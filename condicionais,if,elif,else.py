#Exemplo velocidade em uma via de limite maximo permitido 110km 
velocidade = 111
if velocidade > 110:
    print("Você está trafegando acima da velocidade permitida!!!")
    print("Favor reduza a velocidade para dentro do limite permitido!!!")
elif velocidade < 55:
    print('Você está trafegando abaixo do limite minimo de velocidade permitido!!!')
    print('Favor suba a velocidade para dentro do limite permitido!!!')
else :
    velocidade >= 55 and velocidade >= 110 
    print('Velocidade OK')
