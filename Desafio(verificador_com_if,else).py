# Desafio verificador com if else

'''
Para este desfio peça para o usuario que digite um numero.
Se o numero for maior do que 10 imprima "O numero é maior do que 10"
caso contrario imprima "O numero é menor ou igual a 10".
'''

num = int(input('Digite um numero: '))

if num > 10:
    print("O numero é maior do que 10 ")
else:
    print("O numero é menor ou igual a 10 ")